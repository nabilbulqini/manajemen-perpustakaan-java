   private void cariBuku() {
    String keyword = FieldPencarian.getText().trim().toLowerCase();

    // kalau placeholder atau kosong â†’ tampilkan semua
    if (keyword.equals("pencarian...") || keyword.isEmpty()) {
        loadData();
        return;
    }

    DefaultTableModel model =
        (DefaultTableModel) DataTabelBuku.getModel();
    model.setRowCount(0);

    FileManager fm = new FileManager();
    List<BukuPelajaran> hasil = fm.cariBuku(keyword);

    if (hasil.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Data tidak ditemukan");
        return;
    }

    for (BukuPelajaran b : hasil) {
        model.addRow(new Object[]{
            b.getKode(),
            b.getJudul(),
            b.getPenulis(),
            b.getTahun(),
            b.getJenis(),
            b.getStatus()
        });
    }
}
   





