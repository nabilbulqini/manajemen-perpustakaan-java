    private void ButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonTambahActionPerformed
        // TODO add your handling code here:
      try {
    int tahun = Integer.parseInt(FieldTahun.getText().trim());

    JenisBuku jenis = JenisBuku.valueOf(
        ComboBoxJenis.getSelectedItem().toString()
    );

    StatusBuku status = StatusBuku.TERSEDIA;


    BukuPelajaran b = new BukuPelajaran (
        FieldKodeBuku.getText(),
        FieldJudul.getText(),
        FieldPenulis.getText(),
        tahun,
        jenis,
        status
    );

    new FileManager().simpan(b);

    JOptionPane.showMessageDialog(this, "Buku berhasil ditambahkan");
    new KelolaBuku().setVisible(true);
    dispose();

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Tahun harus angka!");
}
    }//GEN-LAST:event_ButtonTambahActionPerformed

