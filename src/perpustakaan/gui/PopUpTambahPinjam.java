    //method membaca data buku lalu kirim ke combo box
    private void loadComboBuku() {
    ComboBoxKodeBuku.removeAllItems();

    FileManager fm = new FileManager();
    List<BukuPelajaran> list = fm.bacaSemua();

    for (BukuPelajaran b : list) {
        if (b.getStatus() == StatusBuku.TERSEDIA) {
            ComboBoxKodeBuku.addItem(b.getKode());
        }
    }
}
    

    // method membaca data anggota lalu kirim ke combo box
    private void loadComboAnggota() {
    ComboBoxIdAnggota.removeAllItems();

    FileManager fm = new FileManager();
    List<Anggota> list = fm.bacaSemuaAnggota();

    for (Anggota a : list) {
        ComboBoxIdAnggota.addItem(a.getIdAnggota());
    }
}


